# Concatenating two lists using "+" operator
lst = [1,2,3]
lst1 = [4,5,6]
c = lst + lst1
print(c)

# Print repetitive integers in list using "*" operator
l1 = [0,1]
l1 = l1 * 4
print(l1)

# Len() func gives the length of a list
print(len(lst))

# max() func gives the maximum element in the list
print(max(lst1))

# min() func gives the minimum value in the list
print(min(lst1))

# sum() func gives the sum of all the lements in the list
print(sum(lst1))

# How to find average of a list
print(sum(lst1)/len(lst1))
